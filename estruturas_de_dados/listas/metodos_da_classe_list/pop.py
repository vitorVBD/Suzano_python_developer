linguagens = ['Python', 'Java', 'C#', 'JavaScript', 'PHP', 'Ruby', 'C++', 'C', 'Swift', 'Kotlin']

# Remove o último elemento da lista
linguagens.pop()

print(linguagens) # ['Python', 'Java', 'C#', 'JavaScript', 'PHP', 'Ruby', 'C++', 'C', 'Swift']

linguagens.pop(2) # Remove o elemento na posição 2

print(linguagens) # ['Python', 'Java', 'JavaScript', 'PHP', 'Ruby', 'C++', 'C', 'Swift']